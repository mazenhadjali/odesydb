package org.example.entities;

public class Superficie {

    public Long id;
    public Double prop;
    public Double louer;
    public Double surChiites;
    public Double total;
    public Double nombre;

    public Superficie() {
    }


    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Double getProp() {
        return prop;
    }

    public void setProp(Double prop) {
        this.prop = prop;
    }

    public Double getLouer() {
        return louer;
    }

    public void setLouer(Double louer) {
        this.louer = louer;
    }

    public Double getSurChiites() {
        return surChiites;
    }

    public void setSurChiites(Double surChiites) {
        this.surChiites = surChiites;
    }

    public Double getTotal() {
        return total;
    }

    public void setTotal(Double total) {
        this.total = total;
    }

    public Double getNombre() {
        return nombre;
    }

    public void setNombre(Double nombre) {
        this.nombre = nombre;
    }
}
